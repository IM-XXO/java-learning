/**
 * 某老师要录入班级学生信息（姓名，性别，年龄,成绩）
 */
public class Student {
    public String name;
    public String sex;
    public int age;
    public double score;
    public Student(String name,String sex,int age,double score){
        this.name = name;
        this.sex = sex;
        this.age = age;
        this.score = score;
    }
}
