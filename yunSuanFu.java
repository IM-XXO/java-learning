/**
 * 运算符操作
 */
public class yunSuanFu {

    public static void main(String[] args){
        int a = 5,b = 10;
        int c = a + b;
        int d = a - b;
        int e = a * b;
        int f = a / b;
        System.out.println("a+b=" + c);
        System.out.println("a-b=" + d);
        System.out.println("a*b=" + e);
        System.out.println("a/b=" + f);
    }
}
